#string slicing
name = "Aamani"
print(name[0:3])
print(name[:6])
print(name[0:])
print(name[0:5])

#Negative String slicing
name = "python"
print(name[-6:-1])
print(name[-6:-3])
print(name[::-1])

#reverse a list items
name = ["Aamani","teja","anji","ammu"]
name.reverse()
print(name)















